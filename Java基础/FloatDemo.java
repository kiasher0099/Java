package com.imooc;

public class FloatDemo {

	public static void main(String[] args) {
		// Define a float variable, store 3.14
		float pi = 3.14f;
		System.out.println("pi="+pi);
		// Define a double variable, store 0.12345678
		double d = 0.12345678;
		System.out.println("d="+d);
		// Define a double variable, assign an integer value to it
		double intd = 123;
		System.out.println("intd="+intd);
		// Define a double variable, assign an float variable to it
		double floatd = pi;
		System.out.println("floatd="+floatd+"\nHere an miscalculation occurred");
		
	}
}
